static
void	stt_draw_char(t_mat32 frame, uint32_t _x, uint32_t _y, uint8_t c, uint32_t color)
{
	const uint8_t	*glyph;
	size_t			row;
	size_t			col;
	size_t			x;
	size_t			y;

	if (c < 32 || c >= 127)
		glyph = ascii_font['?' - 32];
	else
		glyph = ascii_font[c - 32];
	row = 0;
	while (row < 7)
	{
		y = _y + row;
		if (y >= frame.height)
			break;
		col = 0;
		while (col < 5)
		{
			x = _x + col;
			if (x >= frame.width)
				break ;
			if (glyph[row] & (1u << (4 - col)))
				frame.ptr[y * frame.width + x] = color;
			col++;
		}
		row++;
	}
}

void	draw_ascii(t_mat32 frame, uint32_t x, uint32_t y, const char *str, uint32_t color)
{
	size_t	i;

	if (str == NULL || y >= frame.height)
		return ;
	i = 0;
	while (str[i] != 0 && x < frame.width)
	{
		stt_draw_char(frame, x, y, (uint8_t)str[i], color);
		x += 6;
		i++;
	}
}

void	draw_number(t_mat32 frame, size_t xpos, size_t ypos, uint32_t value, uint32_t color)
{
	char			buffer[24];
	const size_t	length = ft_itoa_r(value, buffer);

	draw_ascii(frame, xpos, ypos, buffer, color);
}
